            if cmd == 'emotions':
                print()
                for name, val in field.emotions.items():
                    print(f"  {name:10}  {bar(val)}  {val:.3f}")
                print(f"  {'will_lie':10}  {bar(field.will_lie)}  {field.will_lie:.3f}")
